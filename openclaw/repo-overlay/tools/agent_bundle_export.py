SECRET_KEY_RE = re.compile(
    r"(token|secret|password|passphrase|api[_-]?key|private[_-]?key|bot[_-]?token|dsn|webhook|bearer|auth)",
    re.IGNORECASE,
)
PLACEHOLDER_TEMPLATE = "__SECRET_{name}__"

REPO_EXCLUDES = {
    ".git",
    "node_modules",
    "dist",
    "coverage",
    ".turbo",
}

def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Export sanitized OpenClaw agent code/config bundle plus separate secrets env."
    )
    parser.add_argument(
        "--repo-root",
        default=None,
        help="OpenClaw repo root. Defaults to this script's repo.",
    )
    parser.add_argument(
        "--config-dir",
        default=None,
        help="OpenClaw config dir. Defaults to $OPENCLAW_CONFIG_DIR or ~/.openclaw.",
    )
    parser.add_argument(
        "--output-dir",
        default=None,
        help="Output directory for bundle folder, archive, and secrets env. Defaults to current directory.",
    )
    parser.add_argument(
        "--bundle-name",
        default=None,
        help="Bundle name prefix. Defaults to openclaw-agent-bundle-YYYYmmdd-HHMMSS.",
    )
    parser.add_argument(
        "--docker-container",
        default=None,
        help="Optional Docker container name to read config from via docker cp.",
    )
    parser.add_argument(
        "--docker-config-dir",
        default="/home/node/.openclaw",
        help="Config directory inside Docker container when --docker-container is used.",
    )
    parser.add_argument(
        "--repo-baseline-dir",
        default=None,
        help="Optional unpacked baseline source tree. If provided, export only repo files changed relative to this tree.",
    )
    return parser.parse_args()


def shell_quote(value: str) -> str:
    return "'" + value.replace("'", "'\"'\"'") + "'"


def to_env_name(parts: list[str]) -> str:
    raw = "_".join(parts).upper()
    raw = re.sub(r"[^A-Z0-9]+", "_", raw)
    raw = re.sub(r"_+", "_", raw).strip("_")
    if not raw.startswith("SECRET_"):
        raw = f"SECRET_{raw}"
    return raw


class SecretStore:
    def __init__(self) -> None:
        self.values: dict[str, str] = {}
        self.reverse: dict[str, str] = {}

    def placeholder(self, env_name: str, value: str) -> str:
        if not value:
            return value
        existing = self.reverse.get(value)
        if existing:
            return PLACEHOLDER_TEMPLATE.format(name=existing)
        self.values[env_name] = value
        self.reverse[value] = env_name
        return PLACEHOLDER_TEMPLATE.format(name=env_name)

    def write_env(self, path: Path) -> None:
        lines = [
            "# Generated by agent_bundle_export.py",
            "# Keep this file outside the archive.",
            "",
        ]
        for name in sorted(self.values):
            lines.append(f"{name}={shell_quote(self.values[name])}")
        lines.append("")
        path.write_text("\n".join(lines), encoding="utf-8")


def should_treat_as_text(path: Path) -> bool:
    return path.suffix.lower() in {
        ".json",
        ".jsonl",
        ".md",
        ".ts",
        ".js",
        ".mjs",
        ".cjs",
        ".yml",
        ".yaml",
        ".env",
        ".txt",
        ".sh",
        ".py",
    } or path.name in {".env", "Dockerfile"}


def is_secret_key(key: str) -> bool:
    return bool(SECRET_KEY_RE.search(key))


def sanitize_json_value(
    obj,
    store: SecretStore,
    path_parts: list[str],
):
    if isinstance(obj, dict):
        result = {}
        for key, value in obj.items():
            child_parts = path_parts + [key]
            if isinstance(value, str) and value and is_secret_key(key):
                env_name = to_env_name(child_parts)
                result[key] = store.placeholder(env_name, value)
            else:
                result[key] = sanitize_json_value(value, store, child_parts)
        return result
    if isinstance(obj, list):
        return [sanitize_json_value(v, store, path_parts + [str(i)]) for i, v in enumerate(obj)]
    return obj


LINE_SECRET_RE = re.compile(
    r'^(\s*(?:export\s+)?)([A-Za-z_][A-Za-z0-9_]*(?:TOKEN|SECRET|PASSWORD|PASS|API_KEY|BOT_TOKEN|DSN|PRIVATE_KEY|AUTH)[A-Za-z0-9_]*)\s*=\s*(.+?)\s*$'
)
JSON_INLINE_SECRET_RE = re.compile(
    r'("(?P<key>[^"]*(?:token|secret|password|api[_-]?key|bot[_-]?token|private[_-]?key|dsn|auth)[^"]*)"\s*:\s*)"(?P<value>[^"]+)"',
    re.IGNORECASE,
)


def sanitize_text_content(text: str, store: SecretStore, rel_path: Path) -> str:
    if rel_path.suffix.lower() == ".json":
        try:
            parsed = json.loads(text)
        except json.JSONDecodeError:
            parsed = None
        if parsed is not None:
            sanitized = sanitize_json_value(parsed, store, [*rel_path.parts])
            return json.dumps(sanitized, ensure_ascii=False, indent=2) + "\n"

    lines: list[str] = []
    for line_no, line in enumerate(text.splitlines(), start=1):
        line_match = LINE_SECRET_RE.match(line)
        if line_match:
            prefix, key, raw_value = line_match.groups()
            value = raw_value.strip().strip('"').strip("'")
            env_name = to_env_name([*rel_path.parts, key])
            placeholder = store.placeholder(env_name, value)
            lines.append(f"{prefix}{key}={placeholder}")
            continue
        lines.append(line)
    sanitized = "\n".join(lines)
    if text.endswith("\n"):
        sanitized += "\n"

    def repl(match: re.Match[str]) -> str:
        key = match.group("key")
        value = match.group("value")
        env_name = to_env_name([*rel_path.parts, key])
        placeholder = store.placeholder(env_name, value)
        return f'{match.group(1)}"{placeholder}"'

    return JSON_INLINE_SECRET_RE.sub(repl, sanitized)


def copy_and_sanitize_tree(
    source_root: Path,
    relative_path: Path,
    dest_root: Path,
    store: SecretStore,
    sanitize: bool = True,
) -> None:
    src = source_root / relative_path
    dst = dest_root / relative_path
    if src.is_dir():
        dst.mkdir(parents=True, exist_ok=True)
        for child in sorted(src.iterdir()):
            copy_and_sanitize_tree(
                source_root,
                relative_path / child.name,
                dest_root,
                store,
                sanitize=sanitize,
            )
        return
    if src.is_symlink():
        resolved = src.resolve(strict=True)
        if resolved.is_dir():
            raise SystemExit(f"Directory symlinks are not supported: {src}")
        src = resolved
    dst.parent.mkdir(parents=True, exist_ok=True)
    if sanitize and should_treat_as_text(src):
        content = src.read_text(encoding="utf-8")
        dst.write_text(sanitize_text_content(content, store, relative_path), encoding="utf-8")
    else:
        shutil.copy2(src, dst)


def write_import_script(template_path: Path, target_path: Path) -> None:
    target_path.write_text(template_path.read_text(encoding="utf-8"), encoding="utf-8")
    target_path.chmod(0o755)


