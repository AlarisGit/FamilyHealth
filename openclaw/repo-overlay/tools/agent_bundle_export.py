def materialize_config_source(args: argparse.Namespace, config_dir: Path) -> tuple[Path, tempfile.TemporaryDirectory[str] | None]:
    if args.docker_container:
        temp_dir = tempfile.TemporaryDirectory(prefix="agent-bundle-config-")
        staged_root = Path(temp_dir.name) / "config"
        staged_root.mkdir(parents=True, exist_ok=True)
        subprocess.run(
            [
                "docker",
                "cp",
                f"{args.docker_container}:{args.docker_config_dir}/.",
                str(staged_root),
            ],
            check=True,
        )
        return staged_root, temp_dir
    return config_dir, None


def build_manifest(
    repo_sync_files: list[str],
    repo_sync_dirs: list[str],
    repo_remove_paths: list[str],
    config_sync_files: list[str],
    config_sync_dirs: list[str],
    config_remove_paths: list[str],
    repo_root: Path,
    config_dir: Path,
) -> dict:
    return {
        "schema": 1,
        "created_at": datetime.now(timezone.utc).isoformat(timespec="seconds").replace("+00:00", "Z"),
        "repo_root_hint": str(repo_root),
        "config_dir_hint": str(config_dir),
        "repo_sync_files": repo_sync_files,
        "repo_sync_dirs": repo_sync_dirs,
        "repo_remove_paths": repo_remove_paths,
        "config_sync_files": config_sync_files,
        "config_sync_dirs": config_sync_dirs,
        "config_remove_paths": config_remove_paths,
    }


def file_bytes(path: Path) -> bytes:
    src = path
    if src.is_symlink():
        resolved = src.resolve(strict=True)
        if resolved.is_dir():
            raise SystemExit(f"Directory symlinks are not supported: {path}")
        src = resolved
    return src.read_bytes()


def collect_repo_paths(repo_root: Path, repo_sync_dirs: list[str], repo_sync_files: list[str]) -> set[Path]:
    collected: set[Path] = set()
    for rel in repo_sync_files:
        path = repo_root / rel
        if path.exists():
            collected.add(Path(rel))
    for rel_dir in repo_sync_dirs:
        root = repo_root / rel_dir
        for path in root.rglob("*"):
            if path.is_dir():
                continue
            collected.add(path.relative_to(repo_root))
    return collected


def build_repo_delta(
    repo_root: Path,
    baseline_root: Path,
    repo_sync_dirs: list[str],
    repo_sync_files: list[str],
) -> tuple[list[str], list[str], list[str]]:
    current_paths = collect_repo_paths(repo_root, repo_sync_dirs, repo_sync_files)
    baseline_paths = collect_repo_paths(baseline_root, repo_sync_dirs, repo_sync_files)

    changed: list[str] = []
    removed: list[str] = []

    for rel in sorted(current_paths):
        current_file = repo_root / rel
        baseline_file = baseline_root / rel
        if not baseline_file.exists():
            changed.append(rel.as_posix())
            continue
        if file_bytes(current_file) != file_bytes(baseline_file):
            changed.append(rel.as_posix())

    for rel in sorted(baseline_paths - current_paths):
        removed.append(rel.as_posix())

    return changed, [], removed


def main() -> int:
    args = parse_args()
    script_path = Path(__file__).resolve()
    repo_root = Path(args.repo_root or script_path.parents[1]).resolve()
    baseline_repo_root = Path(args.repo_baseline_dir).resolve() if args.repo_baseline_dir else None
    config_dir = Path(
        args.config_dir
        or os.environ.get("OPENCLAW_CONFIG_DIR")
        or (Path.home() / ".openclaw")
    ).resolve()
    output_dir = Path(args.output_dir or Path.cwd()).resolve()
    bundle_name = args.bundle_name or f"openclaw-agent-bundle-{datetime.now().strftime('%Y%m%d-%H%M%S')}"

    bundle_root = output_dir / bundle_name
    archive_path = output_dir / f"{bundle_name}.tar.gz"
    secrets_path = output_dir / f"{bundle_name}.secrets.env"
    if bundle_root.exists():
        shutil.rmtree(bundle_root)
    bundle_root.mkdir(parents=True, exist_ok=True)

    store = SecretStore()
    effective_config_dir, temp_config_dir = materialize_config_source(args, config_dir)
    try:
        repo_dest = bundle_root / "bundle" / "repo"
        config_dest = bundle_root / "bundle" / "config"
        repo_dest.mkdir(parents=True, exist_ok=True)
        config_dest.mkdir(parents=True, exist_ok=True)

        repo_base_dirs = ["src", "tools"]
        repo_base_files = ["docker-compose.yml", "package.json", "pnpm-lock.yaml", "tsconfig.json"]
        repo_base_files = [p for p in repo_base_files if (repo_root / p).exists()]
        repo_base_dirs = [p for p in repo_base_dirs if (repo_root / p).exists()]
        repo_remove_paths: list[str] = []
        if baseline_repo_root is not None:
            repo_sync_files, repo_sync_dirs, repo_remove_paths = build_repo_delta(
                repo_root,
                baseline_repo_root,
                repo_base_dirs,
                repo_base_files,
            )
        else:
            repo_sync_dirs = repo_base_dirs
            repo_sync_files = repo_base_files
        for rel in repo_sync_dirs:
            copy_and_sanitize_tree(repo_root, Path(rel), repo_dest, store, sanitize=False)
        for rel in repo_sync_files:
            copy_and_sanitize_tree(repo_root, Path(rel), repo_dest, store, sanitize=False)

        config_candidates = [
            "openclaw.json",
            "exec-approvals.json",
            "agents/clinic/agent/auth-profiles.json",
            "agents/clinic/agent/models.json",
            "agents/main/agent/auth.json",
            "agents/main/agent/auth-profiles.json",
            "agents/main/agent/models.json",
            "credentials/telegram-default-allowFrom.json",
            "credentials/telegram-pairing.json",
            "devices/paired.json",
            "devices/pending.json",
            "identity/device-auth.json",
            "identity/device.json",
            "extensions/fh-api/index.ts",
            "extensions/fh-api/openclaw.plugin.json",
            "skills/family-health-scheduler/SKILL.md",
            "workspace-clinic/AGENTS.md",
            "workspace-clinic/BOOTSTRAP.md",
            "workspace-clinic/HEARTBEAT.md",
            "workspace-clinic/IDENTITY.md",
            "workspace-clinic/SOUL.md",
            "workspace-clinic/TOOLS.md",
            "workspace-clinic/USER.md",
            "workspace-clinic/.openclaw/workspace-state.json",
        ]
        config_sync_files: list[str] = []
        config_sync_dirs: list[str] = []
        config_remove_paths: list[str] = []
        for rel in config_candidates:
            rel_path = Path(rel)
            src = effective_config_dir / rel_path
            if not src.exists():
                continue
            if src.is_dir():
                config_sync_dirs.append(rel)
            else:
                config_sync_files.append(rel)
            copy_and_sanitize_tree(effective_config_dir, rel_path, config_dest, store)

        manifest = build_manifest(
            repo_sync_files,
            repo_sync_dirs,
            repo_remove_paths,
            config_sync_files,
            config_sync_dirs,
            config_remove_paths,
            repo_root,
            config_dir,
        )
        manifest_path = bundle_root / "bundle" / "manifest.json"
        manifest_path.write_text(json.dumps(manifest, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")

        import_template = repo_root / "tools" / "agent_bundle_import.py"
        write_import_script(import_template, bundle_root / "import_bundle.py")
        write_readme(bundle_root / "README.md")
        store.write_env(secrets_path)

        with tarfile.open(archive_path, "w:gz") as tar:
            tar.add(bundle_root / "bundle", arcname="bundle")
            tar.add(bundle_root / "import_bundle.py", arcname="import_bundle.py")
            tar.add(bundle_root / "README.md", arcname="README.md")
    finally:
        if temp_config_dir is not None:
            temp_config_dir.cleanup()

    print(f"Bundle directory: {bundle_root}")
    print(f"Archive: {archive_path}")
    print(f"Secrets env: {secrets_path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
